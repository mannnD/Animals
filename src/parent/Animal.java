package parent;

public class Animal {
	public String getHabitat() {
		return habitat;
	}

	public String getSkill() {
		return Skill;
	}

	public String getType() {
		return type;
	}

	private String habitat;
	private String Skill;
	private String type;
	
	public Animal(String habitat, String skill, String type) {
		super();
		this.habitat = habitat;
		Skill = skill;
		this.type = type;
	}
	
	

}
